package com.orion.foody;

public class CatatankuData {
    private String id;
    private String nama;
    private int jumlah;
    private String waktu;
    private double karbohidrat;
    private double protein;
    private double garam;
    private double gula;
    private double lemak;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getJumlah() {
        return jumlah;
    }

    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }



    public void setKarbohidrat(double karbohidrat) {
        this.karbohidrat = karbohidrat;
    }

    public double getKarbohidrat() {
        return karbohidrat;
    }

    public double getProtein() {
        return protein;
    }

    public void setProtein(double protein) {
        this.protein = protein;
    }

    public double getGaram() {
        return garam;
    }

    public void setGaram(double garam) {
        this.garam = garam;
    }

    public double getGula() {
        return gula;
    }

    public void setGula(double gula) {
        this.gula = gula;
    }

    public double getLemak() {
        return lemak;
    }

    public void setLemak(double lemak) {
        this.lemak = lemak;
    }
}
